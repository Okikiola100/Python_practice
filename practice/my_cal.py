#!/usr/bin/python3
def add(a, b):

    """
    My addition function

    Args:
    a: integer
    b: integer

    Returns:
    addition of a and b, add(a, b)
    """
    return (a + b)
def sub(a, b):
    
    """
    My substraction function

    Args:
    a: integer
    b: integer

    Return:
    Subtraction of b from a, sub(a, b)
    """
    return (a - b)
def mul(a, b):

    """
    My multiplication function

    Args:
    a: integer
    b: integer

    Return:
    Multiplication of a and b, mul(a, b)
    """
    return (a * b)
def div(a, b):

    """
    My division function

    Args:
    a: integer
    b: integer

    Return:
    Division of a by b, div(a, b)
    """
    return (a / b)
def quot(a, b):

    """
    My quotient division

    Args:
    a: integer
    b: integer

    Return:
    The quotent division of a and b, quot(a, b)
    """
    return (a // b)
def mod(a, b):

    """
    My modulus function

    Args:
    a: integrr
    b: integer

    Return:
    The modulus of a and b
    """
    return (a % b)